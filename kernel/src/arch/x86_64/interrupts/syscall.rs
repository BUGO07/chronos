pub fn syscall_handler(id: SyscallId, a0: u64, a1: u64, a2: u64, a3: u64, a4: u64, a5: u64) -> u64 {
    match id {
        SyscallId::Exit => {
            let pid = crate::scheduler::current_process()
                .unwrap()
                .lock()
                .get_pid();
            info!("process {} exited with code {}", pid, a0);
            crate::scheduler::kill_process(pid);
        }
        SyscallId::SchedYield => {
            crate::scheduler::thread::yld();
        }
        SyscallId::Nanosleep => {
            crate::scheduler::thread::sleep(a0);
        }
        SyscallId::Write => {
            let fd = a0;
            let buf = a1 as *const u8;
            let count = a2;

            if fd == 1 {
                let slice = unsafe { core::slice::from_raw_parts(buf, count as usize) };
                if let Ok(s) = core::str::from_utf8(slice) {
                    print!("{}", s);
                } else {
                    print!("{:?}", slice);
                }
                return count;
            } else {
                unimplemented!()
            }
        }
        _ => {
            info!(
                "syscall {} with args {} {} {} {} {} {}",
                id as u64, a0, a1, a2, a3, a4, a5
            );
        }
    }
    0
}
